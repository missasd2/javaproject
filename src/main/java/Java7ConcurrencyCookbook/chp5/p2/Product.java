package Java7ConcurrencyCookbook.chp5.p2;

/**
 * 存储产品的名称与价格
 */
public class Product {
    private String name;
    private double price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
